            <h4 id="Cachviet"><b>III. Cách viết CV xin việc nhân viên phục vụ​​</b></h4>
            <h5 id="Thongtincanhan"><b>1. Thông tin cá nhân</b></h5>
            <p>
              Chức năng của phần này chỉ để nhà tuyển dụng biết họ tên, tuổi tác của ứng viên và cách thức liên hệ nếu
              muốn mời phỏng vấn. Bạn không cần dành quá nhiều thời gian để viết nội dung trong phần thông tin cá nhân
              nhưng hãy chắc chắn đừng phạm bất cứ sai lầm nào nhé, nhất là lỗi chính tả hoặc sai, thiếu số điện thoại,
              sai địa chỉ email hay tệ hơn là sai tên và vị trí ứng tuyển.
            </p>
            <img src="" alt="">
            <h5 id="Muctieunghenghiep"><b>2. Mục tiêu nghề nghiệp</b></h5>
            <p>
              Sẽ rất không hợp lý nếu một nhân viên phục vụ chỉ vừa đi làm lại viết về mục tiêu mở nhà hàng 5 sao trong
              tương lai trong CV xin việc hoặc viết rằng mình muốn làm nhà khoa học, nhà nghiên cứu... Dù cho đó là mục
              tiêu của bạn (vì việc làm nhân viên phục vụ có thể chỉ là việc làm thêm) thì hãy chú ý viết các kế hoạch
              ngắn hạn bạn muốn thực hiện.
            </p>
            <br>
            <p>
              Căn cứ vào mục tiêu của cá nhân bạn, hãy viết phần này trong CV xin việc nhân viên phục vụ thật chân thành
              nhưng hợp lý. Dù bạn nói rằng mình muốn có một công việc làm thêm ổn định, có thu nhập để trang trải học
              phí hay muốn trở thành trưởng ca, giám sát sau 2, 3 năm làm việc đều được.
            </p>
            <br>
            <b>Gợi ý:</b>
            <ul>
              <li><i>Thích nghi nhanh với môi trường làm việc, cung cấp dịch vụ chất lượng theo quy trình, quy định của
                  nhà hàng/cửa hàng, thu hút thêm nhiều khách hàng;</i></li>
              <li><i>Rèn luyện khả năng giao tiếp bằng ngoại ngữ, phát triển kỹ năng chăm sóc khách hàng.</i></li>
              <li><i>Được trở thành nhân viên chính thức sau 3 - 6 tháng làm part-time.</i></li>
            </ul>
            <h5 id="Kinhnghiem"><b>3. Kinh nghiệm</b></h5>
            <p><b>3.1. Với ứng viên có kinh nghiệm</b></p>
            <p>
              CV xin việc nhân viên phục vụ của một ứng viên đã có kinh nghiệm làm việc trong đúng vai trò ứng tuyển sẽ
              rất dễ viết, chủ yếu là vì không lo CV trống trải, thiếu thông tin. Bên cạnh đó, bạn cũng có thể dễ dàng
              thuyết phục nhà tuyển dụng vì bạn đã từng làm nhân viên phục vụ, hiểu rõ quy trình từ chào đón, tiếp nhận
              thông tin order đồ ăn, thức uống đến phối hợp nhịp nhàng với bộ phận bếp, thu ngân... Nhà tuyển dụng
              thường thích tuyển ứng viên có kinh nghiệm cho vị trí nhân viên phục vụ.<br>
              Bên cạnh đó, cũng có một "kiểu" kinh nghiệm khác đó là ứng viên chưa từng làm nhân viên phục vụ nhưng đã
              thử sức trong các vai trò khác như nhân viên bán hàng, telesales, chăm sóc khách hàng, thu ngân siêu
              thị... Lúc này, bạn hãy cân nhắc: Nếu có nhiều kinh nghiệm làm nhân viên phục vụ thì không cần đề cập tới
              kinh nghiệm khác; còn nếu không, có thể đưa kinh nghiệm trái ngành nhưng qua đó thể hiện rằng bạn có kỹ
              năng chuyển đổi, khả năng thích ứng nhanh, trung thực và chăm chỉ.<br>
              Ngoài ra, hãy lưu ý rằng bạn đừng nên liệt kê quá 5 kinh nghiệm, bao gồm cả những kinh nghiệm quá ngắn
              (dưới 6 tháng), đồng thời cũng đừng viết những điều quá "đao to búa lớn" khi trình bày thành tích trong
              công việc.
            </p>
            <b>Gợi ý:</b> Tiệm trà sữa ABC, Nhân viên phục vụ part-time (2/2020 - nay)
            <ul>
              <li><i>Pha chế và phục vụ trà sữa, đồ uống cho khách dùng tại quán và gửi các đơn online.</i></li>
              <li><i>Được quản lý khen thưởng sau 6 tháng làm việc nhờ đi làm chuyên cần, chưa bao giờ phải nhận
                  feedback xấu của khách.</i></li>
            </ul>
            <img src="" alt="">
            <p><b>3.2. Với ứng viên chưa có kinh nghiệm</b></p>
            <p>
              Trên thực tế, việc làm nhân viên phục vụ không quá kén người, không nhất định phải là người có kinh nghiệm
              mới có thể làm được. Bạn là sinh viên vừa tốt nghiệp hay còn trên ghế nhà trường đều có thể ứng tuyển, do
              đó, nếu chưa có kinh nghiệm cũng đừng lo lắng - vẫn có cách để viết CV xin việc nhân viên phục vụ thật
              chuẩn. Chỉ cần có trải nghiệm đi thực tập hoặc tham gia một số sự kiện đơn giản ở trường, chẳng hạn như hỗ
              trợ cho câu lạc bộ, hội thảo,... Trong tình huống này, bạn cần viết đơn giản nhất và nhà tuyển dụng sẽ xem
              xét dựa trên kỹ năng, ngoại hình của bạn.
            </p>
            <b>Gợi ý:</b> Lễ tân trong các sự kiện của trường (kỷ niệm thành lập Đoàn, hoạt động của Hội Sinh viên) -
            2018 - nay
            <ul>
              <li><i>Hỗ trợ chuẩn bị set up hội trường, phòng họp, chào mừng và các công việc khác như tặng hoa, quà lưu
                  niệm cho khách mời.</i></li>
              <li><i>Thoải mái khi tiếp xúc với nhiều người, có kỹ năng giao tiếp và xử lý tình huống thành thạo.</i>
              </li>
            </ul>
            <h5 id="hocvan"><b>4. Học vấn</b></h5>
            <p>
              Học vấn, bằng cấp chính quy không phải yêu cầu quan trọng nhất của nhà tuyển dụng với nhân viên phục vụ. Ở
              các cửa hàng, nhà hàng nhỏ thì có thể sẽ tuyển nhân viên phục vụ tốt nghiệp THPT, tại nhà hàng lớn hay nhà
              hàng trong khách sạn, nhân viên phục vụ thường được yêu cầu có bằng trung cấp trở lên, tốt nhất là các
              chuyên ngành Du lịch, Nhà hàng - khách sạn,... Các vị trí part - time sẽ tuyển cả sinh viên đang đi học,
              tất cả các chuyên ngành.
              Trong CV xin việc nhân viên phục vụ, phần học vấn cần được viết chính xác bằng cấp hiện tại của ứng viên.
              Nếu bạn là sinh viên, hãy viết rằng mình đang đi học.
            </p>
            <b>Gợi ý:</b>
            <p><i>Cao đẳng Du lịch Hà Nội (2019 - nay)

                Sinh viên năm 2, chuyên ngành: Dịch vụ nhà hàng</i></p>
            <h5 id="kynang"><b>5. Kỹ năng</b></h5>
            <img src="assets/images/a3.jpg" alt="" class="img-fluid"
