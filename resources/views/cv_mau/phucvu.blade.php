              tuyển dụng đã là đủ. Thế nhưng, giới thiệu mình thế nào cho thật ấn tượng và thuyết phục, cho thấy rằng
              bạn có thể đáp ứng các yêu cầu về sức khỏe, sự chăm chỉ, khả năng làm việc theo ca... lại là một câu hỏi
              không dễ tìm ra lời giải đáp.
            </p>
            <p>
              Trước hết, bạn sẽ cần phải xác định được đâu là vấn đề trọng tâm, là thông tin quan trọng nhất mà nhà
              tuyển dụng chắc chắn sẽ chú ý khi xét duyệt CV xin việc nhân viên phục vụ. Về cơ bản, thông tin không thể
              thiếu trong CV nhân viên phục vụ sẽ là <b>kinh nghiệm </b> và <b>kỹ năng phục vụ</b>, cam kết <b>trung
                thực</b>, <b>gắn bó </b> với công việc trong thời gian đủ lâu dài. Sẽ không có một nhà hàng, cửa hàng
              nào muốn nhân viên phục vụ của mình - dù làm thêm hay làm việc toàn thời gian chỉ được vài tháng đã nghỉ.
              Bạn có thể khéo léo lồng ghép các thông tin, từ khóa này vào phần kinh nghiệm, kỹ năng và mục tiêu nghề
              nghiệp.
