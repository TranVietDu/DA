            <h5 id="sothich"><b>6. Sở thích</b></h5>
            <p>
              Sở thích là phần không mấy quan trọng khi viết CV xin việc nhân viên phục vụ, tuy nhiên, đôi khi nội dung
              phần này lại khá hữu ích trong việc khiến nhà tuyển dụng có thiện cảm với bạn. Dù cho sở thích của bạn có
              đơn giản như xem phim, nghe nhạc, chơi game hay đi du lịch thì hãy cứ tự tin chia sẻ nhé.
            </p>
            <h4 id="Tieuchuan"><b>IV. Tiêu chuẩn tuyển dụng nhân viên phục vụ</b></h4>
            <p>
              Mỗi nhà tuyển dụng sẽ có yêu cầu và cách đánh giá riêng đối với các ứng viên vị trí nhân viên phục vụ. Tuy
              vậy, vẫn có một số tiêu chuẩn tuyển dụng cơ bản, được áp dụng ở hầu hết mọi nơi như:
            </p>
            <ul>
              <li>Sức khỏe tốt, nhanh nhẹn và chăm chỉ, chịu khó.</li>
              <li>Cẩn thận, có khả năng đa nhiệm.</li>
              <li>Yêu thích môi trường dịch vụ, có thể làm theo ca.</li>
              <li>Khéo ăn nói, hòa nhã và kiên nhẫn với khách hàng.</li>
