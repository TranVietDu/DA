            <p>
              Nghề nghiệp nhân viên phục vụ không thực sự yêu cầu vấn đề bằng cấp, ngược lại, công việc này vô cùng khắt
              khe về mặt kỹ năng. Nhân viên phục vụ là người theo sát khách hàng, để ý từng chi tiết nhằm cung cấp dịch
              vụ chu đáo, tận tình. Môi trường công việc đòi hỏi ứng viên phải có sức khỏe, cẩn thận trong phục vụ, khéo
              léo trong giao tiếp, thông minh trong đối đáp và xử lý mọi tình huống có thể xảy ra.
            </p>
            <b>Gợi ý:</b>
            <ul>
              <li>Kỹ năng sử dụng hệ thống POS.</li>
              <li>Kỹ năng lắng nghe tích cực và giao tiếp hiệu quả.</li>
              <li>Kỹ năng phục vụ, chăm sóc khách hàng.</li>
              <li>Kỹ năng xử lý tình huống và giải quyết vấn đề.</li>
              <li>Kỹ năng sắp xếp.</li>
              <li>Giữ gìn vệ sinh, an toàn thực phẩm.</li>
            </ul>
